// 一个文件中只能有 1 个公共类  1 个公共类必须跟文件名一致
public class HelloWorld {
    /**
     * public 公共的
     * static 静态的
     * void 表示空
     * main 表示方法名
     * String[] args main 方法的形参
     * 总结:定义了一个公开的静态的无返回值的 main 方法
     */
    public static void main(String[] args) {
        // 向控制台输出一段字符串
        System.out.println("Hello World");
    }
}